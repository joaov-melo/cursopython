# Tipo de dado bool (bolean)
# Ao questionar algo em um programa só existem duas opções possíveis: True ou False
# Existem vários operadores para "questionar"
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro
print(10==10) # Se sim, -> True
print(10==11) # Se não, -> False
print(type(10==10))
print(type(10==11))
print(type(True))
print(type(False))