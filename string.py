'''
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''
print(1234)

# Aspas simples
print('João Vitor')
print(1,'"João Vitor"') # usar aspas diferentes para imprimir

# Aspas duplas
print("João Vitor")
print(2,"'João Vitor'") # usar aspas diferentes para imprimir

# Escape
print("João \"Vitor\"") # \ para não interpretar e
                        # sim imprimir as aspas
# r
print(r"João \"Vitor\"") # para imprimir o símbolo de Escape junto
